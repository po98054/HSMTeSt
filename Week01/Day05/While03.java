package Day05;

import java.util.Scanner;

public class While03 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
	    while(true) {
	    	
	    	char ch = sc.next().charAt(0);
	    	
	    	if (ch == 'q') {
	    		
	    		break;
	    		
	    	}
	    	
	    }
	    
	    sc.close();

	}

}
