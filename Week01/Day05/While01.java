package Day05;

public class While01 {

	public static void main(String[] args) {
		
		for (int i = 1; i < 6; i++) {
			
			System.out.println("Hello James!!!!");
			
		}
		
		System.out.println("------------------");
		
		int i = 1;
		
		while(i < 6) {
			
			System.out.println("Hello SeongMin");
			i++;
			
		}

	}

}
